package application;

public class info {
	private String s;
	private String t;

	public info(String s, String t) {
		super();
		this.s = s;
		this.t = t;
	}

	public String getS() {
		return s;
	}

	public void setS(String s) {
		this.s = s;
	}

	public String getT() {
		return t;
	}

	public void setT(String t) {
		this.t = t;
	}

}
